/*
BOOL VERI TIPI (true ve ya false)

# Mantiksal ifadeler dogru ise true degerini dondurur.
# Mantiksal ifadeler yanlis ise false degerini dondurur.

NOT: Butun '0' olmayan sayilar 'true' deger sayilir. 0 ise 'false' degerdir.
*/



/*
MANTIKSAL OPERATORLER:

< : Kucuk mudur?  a<b seklinde.
> : Buyuk mudur?  a>b seklinde.
<= : Kucuk veya esit midir?  a<=b seklinde.
>= : Buyuk veya esit midir?  a>=b seklinde.
== : Esit midir?  a==b seklinde.
!= : Esit degil midir?  a!=b seklinde.
*/



/*
MANTIKSAL BAGLACLAR:

# Birçok mantiksal ifadeyi/islemi baglayan araclardir.

&& : Mantiksal and(ve) baglaci.
- Cumle ya tamamen 'true' dir ya da tamamen 'false' dir.
- Icınde oldugu cumle true iken bir tane false olması sonucu false yapar.


|| : Mantiksal or(ya da) baglaci.
- Cumlenin true olmasi icin en az bir tane 'true' icermesi gerekir.
- Tum ifadelerin icerisinde bir tane bile 'true' yoksa sonuc false olur.


! : Mantiksal not(degili) baglaci.
- True olan bir ifadeyi tersine cevirir(false yapar).
- False olan bir ifadeyi tersine cevirir(true yapar).
*/
